        /// <summary>
        ///     compute the logarithm of base 2 for a 32-bit number
        /// </summary>
        /// <param name="val">value to evaluate</param>
        /// <returns>log2 of the value</returns>
        public static uint LogBase2(this uint val) {
            var temp = val >> 24;
            if (temp != 0)
                return 24U + logTable[temp];

            temp = val >> 16;
            if (temp != 0)
                return 16U + logTable[temp];

            temp = val >> 8;
            if (temp != 0)
                return 8U + logTable[temp];

            return logTable[val];
        }

